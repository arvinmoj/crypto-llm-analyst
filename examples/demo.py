async def example_mcp_protocol():
    """MCP protocol example."""
    logger.info("=== MCP Protocol Example ===")
    
    config = load_config_from_env()
    system = CryptoLLMAnalyst(config)
    
    try:
        await system.initialize()
        
        if not system.mcp_protocol:
            logger.info("MCP protocol not initialized")
            return
        
        # Example MCP messages
        initialize_request = {
            "id": "req_001",
            "type": "request",
            "method": "initialize",
            "params": {}
        }
        
        list_tools_request = {
            "id": "req_002",
            "type": "request",
            "method": "list_tools",
            "params": {}
        }
        
        # Handle messages
        logger.info("Sending MCP initialize request...")
        init_response = await system.mcp_protocol.handle_message(initialize_request)
        logger.info(f"Initialize response: {init_response}")
        
        logger.info("Sending list tools request...")
        tools_response = await system.mcp_protocol.handle_message(list_tools_request)
        logger.info(f"Available tools: {len(tools_response.get('result', {}).get('tools', []))}")
        
    finally:
        await system.shutdown()


