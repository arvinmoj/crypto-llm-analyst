async def example_comprehensive_analysis():
    """Comprehensive analysis example using multiple components."""
    logger.info("=== Comprehensive Analysis Example ===")
    
    config = load_config_from_env()
    system = CryptoLLMAnalyst(config)
    
    try:
        await system.initialize()
        
        # Perform different types of analysis
        analyses = [
            ("general", "What is the overall market sentiment for Bitcoin?"),
            ("technical", "What do the technical indicators show for BTCUSDT?"),
            ("prediction", "Where do you see Bitcoin price in the next 24 hours?"),
            ("market", "Should I buy, sell, or hold Bitcoin right now?")
        ]
        
        for analysis_type, query in analyses:
            if system.llm_manager:
                logger.info(f"Performing {analysis_type} analysis...")
                result = await system.analyze_market(
                    symbol="BTCUSDT",
                    query=query,
                    analysis_type=analysis_type
                )
                
                logger.info(f"{analysis_type.title()} Analysis:")
                logger.info(f"  Query: {query}")
                logger.info(f"  Confidence: {result.get('confidence', 0):.1%}")
                logger.info(f"  Result: {result.get('analysis', 'No result')[:150]}...")
                logger.info("")
            else:
                logger.info(f"Skipping {analysis_type} analysis (LLM not available)")
        
    finally:
        await system.shutdown()


