async def example_basic_usage():
    """Basic usage example."""
    logger.info("=== Basic Usage Example ===")
    
    # Load configuration from environment
    config = load_config_from_env()
    
    # For demo purposes, you can override with dummy values if env vars not set
    if not config.get("openai_api_key"):
        logger.warning("OpenAI API key not found, using dummy key for demo")
        config["openai_api_key"] = "sk-dummy-key-for-demo"
    
    # Initialize system
    system = CryptoLLMAnalyst(config)
    
    try:
        # Initialize components
        success = await system.initialize()
        if not success:
            logger.error("Failed to initialize system")
            return
        
        # Check system status
        status = system.get_system_status()
        logger.info(f"System Status: {status}")
        
        # Example: Get market summary (requires database)
        if system.database:
            logger.info("Getting market summary...")
            summary = await system.get_market_summary("BTCUSDT")
            logger.info(f"Market Summary: {summary}")
        else:
            logger.info("Database not available, skipping market summary")
        
        # Example: Perform analysis (requires LLM)
        if system.llm_manager:
            logger.info("Performing market analysis...")
            analysis = await system.analyze_market(
                symbol="BTCUSDT",
                query="What is the current trend for Bitcoin?",
                analysis_type="general"
            )
            logger.info(f"Analysis Result: {analysis.get('analysis', 'No analysis available')[:200]}...")
        else:
            logger.info("LLM manager not available, skipping analysis")
        
    finally:
        await system.shutdown()


